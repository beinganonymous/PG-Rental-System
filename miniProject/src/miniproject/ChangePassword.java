    private void buttonConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonConfirmActionPerformed
        // TODO add your handling code here:
        OP = jTextOP.getText();
        NP = jTextNP.getText();
        CP = jTextCP.getText();
        if (OP.equals(_pass)) {
            if (CP.equals(NP) && NP != null && CP != null) {

                try {
                    String query;
                    if (entity == "CUSTOMER") {
                        query = "update customer set password= ? where customer_id=" + id;

                    } else {
                        query = "update proprietor set password= ? where proprietor_id=" + id;

                    }

                    PreparedStatement statement;
                    statement = con.prepareStatement(query);
                    statement.setString(1, NP);
                    int rs = statement.executeUpdate();
                    dispose();
                } catch (Exception ex) {
                    jLabelToast.setText("An error occured");

                    Logger.getLogger(ChangePassword.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                jLabelToast.setText("The new passwords don't match");
            }

        } else {
            jLabelToast.setText("Wrong Password");
        }
    }//GEN-LAST:event_buttonConfirmActionPerformed
