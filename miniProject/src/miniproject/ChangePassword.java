    private void connection(String myid) {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            DriverManager.registerDriver(new org.apache.derby.jdbc.ClientDriver());
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/database", user, pass);
            String query;
            if (entity == "CUSTOMER") {
                query = "select PASSWORD from customer where customer_id=" + myid;

            } else {
                query = "select PASSWORD from proprietor where proprietor_id=" + myid;

            }
            PreparedStatement statement = con.prepareStatement(query);
            ResultSet rs = statement.executeQuery();
            if (rs.next()) {
                _pass = rs.getString(1);
                jLabelToast.setText(_pass);
            }
        } catch (ClassNotFoundException | SQLException e) {
            e.printStackTrace();
        }
    }

